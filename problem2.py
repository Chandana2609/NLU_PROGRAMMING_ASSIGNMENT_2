import torch
import torch.nn as nn
import torch.nn.functional as F
import random

# Load data
with open("TrainingNames.txt", "r") as f:
    names = [line.strip().lower() for line in f]

# Build vocab
chars = sorted(list(set("".join(names))))
chars = ['<SOS>', '<EOS>'] + chars

char_to_idx = {ch: i for i, ch in enumerate(chars)}
idx_to_char = {i: ch for ch, i in char_to_idx.items()}

vocab_size = len(chars)

"""#Encode names"""

encoded_names = []

for name in names:
    seq = [char_to_idx['<SOS>']] + \
          [char_to_idx[ch] for ch in name] + \
          [char_to_idx['<EOS>']]
    encoded_names.append(seq)

inputs = []
targets = []

for seq in encoded_names:
    for i in range(len(seq) - 1):
        inputs.append(seq[:i+1])
        targets.append(seq[i+1])

"""#Data Loader"""

from torch.nn.utils.rnn import pad_sequence
from torch.utils.data import TensorDataset, DataLoader

inputs = [torch.tensor(seq) for seq in inputs]
inputs_padded = pad_sequence(inputs, batch_first=True, padding_value=0)

targets = torch.tensor(targets)

dataset = TensorDataset(inputs_padded, targets)
loader = DataLoader(dataset, batch_size=32, shuffle=True)

"""#Vanilla RNN"""

class RNNModel(nn.Module):
    def __init__(self, vocab_size, embed_dim, hidden_dim):
        super().__init__()

        self.embedding = nn.Embedding(vocab_size, embed_dim, padding_idx=0)
        self.rnn = nn.RNN(embed_dim, hidden_dim, batch_first=True)
        self.fc = nn.Linear(hidden_dim, vocab_size)

    def forward(self, x):
        x = self.embedding(x)
        out, _ = self.rnn(x)
        out = self.fc(out[:, -1, :])
        return out

"""#Training Loop"""

model = RNNModel(vocab_size, 32, 64)

optimizer = torch.optim.Adam(model.parameters(), lr=0.003)
loss_fn = nn.CrossEntropyLoss()

epochs = 10

for epoch in range(epochs):
    total_loss = 0

    for x, y in loader:
        optimizer.zero_grad()

        out = model(x)
        loss = loss_fn(out, y)

        loss.backward()
        optimizer.step()

        total_loss += loss.item()

    print(f"Epoch {epoch+1}, Loss: {total_loss:.4f}")

"""Generated names"""

def generate_name(model, max_len=10):
    model.eval()

    seq = [char_to_idx['<SOS>']]

    for _ in range(max_len):
        x = torch.tensor(seq).unsqueeze(0)

        out = model(x)
        probs = torch.softmax(out, dim=1).detach().numpy()[0]

        # remove SOS from sampling
        valid_indices = list(range(vocab_size))
        valid_indices.remove(char_to_idx['<SOS>'])

        filtered_probs = [probs[i] for i in valid_indices]

        idx = random.choices(valid_indices, weights=filtered_probs)[0]
        char = idx_to_char[idx]

        # stop condition
        if char == '<EOS>' and len(seq) > 3:
            break

        # avoid repetition
        if len(seq) > 2 and idx == seq[-1]:
            continue

        seq.append(idx)

    # clean output
    name = "".join(
        ch for ch in [idx_to_char[i] for i in seq[1:]]
        if ch not in ['<SOS>', '<EOS>']
    )

    return name.capitalize()

