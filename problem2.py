"""#Evaluation metric"""

import pandas as pd
from difflib import SequenceMatcher


# HELPER: SIMILARITY FUNCTION

def similarity(a, b):
    return SequenceMatcher(None, a, b).ratio()


# NORMALIZE NAMES

def normalize(names):
    return [name.lower().strip() for name in names]


# NOVELTY (STRONG VERSION)

def compute_novelty(generated, training, threshold=0.8):
    training = normalize(training)
    generated = normalize(generated)

    novel_count = 0

    for g in generated:
        is_novel = True

        for t in training:
            if similarity(g, t) > threshold:
                is_novel = False
                break

        if is_novel:
            novel_count += 1

    return novel_count / len(generated)


# DIVERSITY

def compute_diversity(generated):
    generated = normalize(generated)
    unique = len(set(generated))
    return unique / len(generated)



# MAIN EVALUATION FUNCTION
def evaluate_model(model_name, generated_names, training_names):
    novelty = compute_novelty(generated_names, training_names)
    diversity = compute_diversity(generated_names)

    return {
        "Model": model_name,
        "Novelty": round(novelty, 4),
        "Diversity": round(diversity, 4)
    }



