def analogy(word1, word2, word3, top_k=5):
    """
    word1 : word2 :: word3 : ?
    """

    if word1 not in word_to_idx or word2 not in word_to_idx or word3 not in word_to_idx:
        print("One of the words not in vocabulary")
        return

    vec = (
        get_embedding(word2)
        - get_embedding(word1)
        + get_embedding(word3)
    )

    similarities = []

    for word in word_to_idx:
        if word not in [word1, word2, word3]:
            word_vec = get_embedding(word)
            sim = torch.dot(vec, word_vec) / (torch.norm(vec) * torch.norm(word_vec))
            similarities.append((word, sim.item()))

    similarities.sort(key=lambda x: x[1], reverse=True)

    return similarities[:top_k]

