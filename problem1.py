"""#PREPROCESSING (clean + tokenize + corpus)"""

import os
import re
import string

# -------------------------------
# CLEANING FUNCTION
# -------------------------------
def clean_text(text):
    """
    Cleans raw text by:
    - Removing HTML tags
    - Removing numbers
    - Removing punctuation
    - Removing extra spaces
    """

    # Remove HTML tags (if any)
    text = re.sub(r'<.*?>', ' ', text)

    # Remove numbers
    text = re.sub(r'\d+', ' ', text)

    # Remove punctuation
    text = text.translate(str.maketrans('', '', string.punctuation))

    # Convert to lowercase
    text = text.lower()

    # Remove extra whitespace
    text = re.sub(r'\s+', ' ', text)

    return text.strip()


# -------------------------------
# TOKENIZATION FUNCTION
# -------------------------------
def tokenize(text):
    """
    Splits cleaned text into words
    """
    return text.split()


# -------------------------------
# PROCESS SINGLE FILE
# -------------------------------
def process_file(file_path):
    """
    Reads and processes a file
    """
    with open(file_path, 'r', encoding='utf-8') as f:
        text = f.read()

    cleaned = clean_text(text)
    tokens = tokenize(cleaned)

    return tokens


# -------------------------------
# BUILD CORPUS
# -------------------------------
def build_corpus(folder_path):
    """
    Reads all files and builds:
    - corpus (list of sentences)
    - all_tokens (flat list)
    """

    corpus = []
    all_tokens = []

    for file in os.listdir(folder_path):
        file_path = os.path.join(folder_path, file)

        tokens = process_file(file_path)

        corpus.append(tokens)
        all_tokens.extend(tokens)

    return corpus, all_tokens


# -------------------------------
# DATASET STATISTICS
# -------------------------------
def dataset_stats(corpus, all_tokens):
    """
    Prints required statistics
    """

    num_documents = len(corpus)
    total_tokens = len(all_tokens)
    vocab = set(all_tokens)
    vocab_size = len(vocab)

    print("Number of documents:", num_documents)
    print("Total tokens:", total_tokens)
    print("Vocabulary size:", vocab_size)

    return num_documents, total_tokens, vocab_size


