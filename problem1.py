import torch
import torch.nn as nn
import torch.optim as optim


# STEP 1: BUILD VOCABULARY

def build_vocab(tokens):
    vocab = list(set(tokens))
    word_to_idx = {word: i for i, word in enumerate(vocab)}
    idx_to_word = {i: word for word, i in word_to_idx.items()}
    return vocab, word_to_idx, idx_to_word



# STEP 2: CREATE TRAINING DATA

def generate_cbow_data(tokens, window_size=2):
    data = []

    for i in range(window_size, len(tokens) - window_size):
        context = []
        for j in range(-window_size, window_size + 1):
            if j != 0:
                context.append(tokens[i + j])

        target = tokens[i]
        data.append((context, target))

    return data


# -------------------------------
# STEP 3: CBOW MODEL

class CBOW(nn.Module):
    def __init__(self, vocab_size, embedding_dim):
        super(CBOW, self).__init__()

        self.embeddings = nn.Embedding(vocab_size, embedding_dim)
        self.linear = nn.Linear(embedding_dim, vocab_size)

    def forward(self, context_idxs):
        embeds = self.embeddings(context_idxs)

        # Average context embeddings
        avg_embed = torch.mean(embeds, dim=0)

        out = self.linear(avg_embed)
        log_probs = torch.log_softmax(out, dim=0)

        return log_probs



# STEP 4: TRAINING FUNCTION

def train_cbow(data, word_to_idx, vocab_size, embedding_dim=100, epochs=10, lr=0.01):

    model = CBOW(vocab_size, embedding_dim)
    loss_function = nn.NLLLoss()
    optimizer = optim.SGD(model.parameters(), lr=lr)

    for epoch in range(epochs):
        total_loss = 0

        for context, target in data:

            context_idxs = torch.tensor([word_to_idx[w] for w in context], dtype=torch.long)
            target_idx = torch.tensor([word_to_idx[target]], dtype=torch.long)

            model.zero_grad()

            log_probs = model(context_idxs)

            loss = loss_function(log_probs.view(1, -1), target_idx)

            loss.backward()
            optimizer.step()

            total_loss += loss.item()

        print(f"Epoch {epoch+1}, Loss: {total_loss:.4f}")

    return model


