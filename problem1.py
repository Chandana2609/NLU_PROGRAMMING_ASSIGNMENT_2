import torch
import torch.nn as nn
import torch.optim as optim
import random

# -------------------------------
# STEP 1: GENERATE SKIPGRAM DATA
# -------------------------------
def generate_skipgram_data(tokens, window_size=2):
    pairs = []

    for i in range(window_size, len(tokens) - window_size):
        target = tokens[i]

        for j in range(-window_size, window_size + 1):
            if j != 0:
                context = tokens[i + j]
                pairs.append((target, context))

    return pairs


# -------------------------------
# STEP 2: MODEL
# -------------------------------
class SkipGramNeg(nn.Module):
    def __init__(self, vocab_size, embedding_dim):
        super(SkipGramNeg, self).__init__()

        # Input embedding (target word)
        self.in_embeddings = nn.Embedding(vocab_size, embedding_dim)

        # Output embedding (context word)
        self.out_embeddings = nn.Embedding(vocab_size, embedding_dim)

    def forward(self, target_idx, context_idx, negative_idxs):
        target_embed = self.in_embeddings(target_idx)
        context_embed = self.out_embeddings(context_idx)

        # Positive score
        pos_score = torch.dot(target_embed, context_embed)
        pos_loss = torch.log(torch.sigmoid(pos_score))

        # Negative sampling loss
        neg_embed = self.out_embeddings(negative_idxs)
        neg_score = torch.matmul(neg_embed, target_embed)

        neg_loss = torch.sum(torch.log(torch.sigmoid(-neg_score)))

        return -(pos_loss + neg_loss)


# -------------------------------
# STEP 3: NEGATIVE SAMPLING
# -------------------------------
def get_negative_samples(vocab_size, k):
    return torch.tensor(random.sample(range(vocab_size), k), dtype=torch.long)


# -------------------------------
# STEP 4: TRAINING
# -------------------------------
def train_skipgram(pairs, word_to_idx, vocab_size, embedding_dim=100, epochs=5, lr=0.01, neg_samples=5):

    model = SkipGramNeg(vocab_size, embedding_dim)
    optimizer = optim.SGD(model.parameters(), lr=lr)

    for epoch in range(epochs):
        total_loss = 0

        for target, context in pairs:

            target_idx = torch.tensor(word_to_idx[target], dtype=torch.long)
            context_idx = torch.tensor(word_to_idx[context], dtype=torch.long)

            negative_idxs = get_negative_samples(vocab_size, neg_samples)

            model.zero_grad()

            loss = model(target_idx, context_idx, negative_idxs)

            loss.backward()
            optimizer.step()

            total_loss += loss.item()

        print(f"Epoch {epoch+1}, Loss: {total_loss:.4f}")

    return model


